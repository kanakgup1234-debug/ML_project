const express = require('express');
const router = express.Router();
const fs = require('fs');
const path = require('path');
const csvParser = require('csv-parser');
const Student = require('../models/Student');

const MASTER_CSV_PATH = path.join(__dirname, '..', '..', 'output', 'master_performance.csv');
const RANKINGS_CSV_PATH = path.join(__dirname, '..', '..', 'output', 'final_rankings.csv');
const SUMMARY_CSV_PATH = path.join(__dirname, '..', '..', 'output', 'module_summary.csv');

// Helper function to check if server is in database mode
const isDbConnected = () => {
  return global.dbConnected === true;
};

// Helper: Parse CSV file into an array of objects
const parseCSV = (filePath) => {
  return new Promise((resolve, reject) => {
    if (!fs.existsSync(filePath)) {
      return resolve([]);
    }
    const results = [];
    fs.createReadStream(filePath)
      .pipe(csvParser())
      .on('data', (data) => results.push(data))
      .on('end', () => resolve(results))
      .on('error', (err) => reject(err));
  });
};

// Route 1: Get all students leaderboard
router.get('/', async (req, res) => {
  try {
    if (isDbConnected()) {
      const students = await Student.find().sort({ rank: 1 });
      return res.json(students);
    } else {
      const data = await parseCSV(RANKINGS_CSV_PATH);
      const formatted = data.map((row) => ({
        name: row.Name,
        email: row.Email,
        cumulativeScore: parseFloat(row.Cumulative_Score),
        cumulativeMax: parseFloat(row.Cumulative_Max),
        cumulativePct: parseFloat(row.Cumulative_Pct),
        finalPercentile: parseFloat(row.Final_Percentile),
        rank: parseInt(row.Rank, 10),
        grade: row.Grade,
      }));
      return res.json(formatted);
    }
  } catch (err) {
    console.error('Error fetching students:', err);
    return res.status(500).json({ error: 'Server error fetching student list' });
  }
});

// Route 2: Get analytics & summary statistics
router.get('/stats', async (req, res) => {
  try {
    let students = [];
    let summaryData = [];

    if (isDbConnected()) {
      students = await Student.find();
      // We will parse the summary CSV for module averages since it's already generated by python
      summaryData = await parseCSV(SUMMARY_CSV_PATH);
    } else {
      students = await parseCSV(RANKINGS_CSV_PATH);
      summaryData = await parseCSV(SUMMARY_CSV_PATH);
    }

    if (students.length === 0) {
      return res.json({
        totalStudents: 0,
        avgPercentage: 0,
        highestPercentage: 0,
        passRate: 0,
        gradeCounts: {},
        moduleAverages: []
      });
    }

    // Calculations
    const totalStudents = students.length;
    let pctSum = 0;
    let highestPct = -1;
    let lowestPct = 101;
    let highestScorer = null;
    let lowestScorer = null;
    let passingCount = 0;
    const gradeCounts = {};

    students.forEach((s) => {
      const name = isDbConnected() ? s.name : s.Name;
      const email = isDbConnected() ? s.email : s.Email;
      const rank = parseInt(isDbConnected() ? s.rank : s.Rank, 10);
      const pct = parseFloat(isDbConnected() ? s.cumulativePct : s.Cumulative_Pct);
      const grade = isDbConnected() ? s.grade : s.Grade;

      pctSum += pct;
      if (pct > highestPct) {
        highestPct = pct;
        highestScorer = { name, email, cumulativePct: pct, grade, rank };
      }
      if (pct < lowestPct) {
        lowestPct = pct;
        lowestScorer = { name, email, cumulativePct: pct, grade, rank };
      }
      if (grade !== 'F') passingCount++;
      
      gradeCounts[grade] = (gradeCounts[grade] || 0) + 1;
    });

    const avgPercentage = pctSum / totalStudents;
    const passRate = (passingCount / totalStudents) * 100;

    const moduleAverages = summaryData.map((row) => ({
      name: row['Module Name'],
      avgPct: parseFloat(row['Average Percentage'])
    }));

    let totalQuizzes = 0;
    let recentUploads = [];
    try {
      const quizzesDir = path.join(__dirname, '..', '..', 'quizzes');
      if (fs.existsSync(quizzesDir)) {
        const files = fs.readdirSync(quizzesDir).filter(f => f.endsWith('.csv'));
        totalQuizzes = files.length;
        recentUploads = files.map(file => {
          const fileStats = fs.statSync(path.join(quizzesDir, file));
          const d = fileStats.mtime;
          const pad = n => String(n).padStart(2, '0');
          const dateStr = `${pad(d.getDate())}/${pad(d.getMonth() + 1)}/${d.getFullYear()}, ${pad(d.getHours())}:${pad(d.getMinutes())}:${pad(d.getSeconds())}`;
          return { filename: file, dateStr, mtime: fileStats.mtime };
        }).sort((a, b) => b.mtime - a.mtime)
          .map(f => ({ filename: f.filename, dateStr: f.dateStr }));
      }
    } catch (e) {
      console.error('Error reading quizzes dir for stats:', e);
    }

    return res.json({
      totalStudents,
      avgPercentage,
      highestPercentage: highestPct,
      passRate,
      gradeCounts,
      moduleAverages,
      highestScorer,
      lowestScorer,
      totalQuizzes,
      recentUploads
    });
  } catch (err) {
    console.error('Error fetching statistics:', err);
    return res.status(500).json({ error: 'Server error fetching statistics' });
  }
});

// Route 3: Get remedial students list
router.get('/remedial', async (req, res) => {
  try {
    if (isDbConnected()) {
      const students = await Student.find({
        $or: [
          { cumulativePct: { $lt: 50 } },
          { grade: { $in: ['D', 'F'] } }
        ]
      }).sort({ rank: 1 });
      return res.json(students);
    } else {
      const data = await parseCSV(RANKINGS_CSV_PATH);
      const filtered = data
        .filter((row) => {
          const pct = parseFloat(row.Cumulative_Pct);
          const grade = row.Grade;
          return pct < 50 || grade === 'D' || grade === 'F';
        })
        .map((row) => ({
          name: row.Name,
          email: row.Email,
          cumulativePct: parseFloat(row.Cumulative_Pct),
          grade: row.Grade,
          rank: parseInt(row.Rank, 10)
        }));
      return res.json(filtered);
    }
  } catch (err) {
    console.error('Error fetching remedial list:', err);
    return res.status(500).json({ error: 'Server error fetching remedial list' });
  }
});

