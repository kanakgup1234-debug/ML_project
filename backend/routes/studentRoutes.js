        cumulativePct: parseFloat(studentRow.Cumulative_Pct),
        finalPercentile: parseFloat(studentRow.Final_Percentile),
        rank: parseInt(studentRow.Rank, 10),
        grade: studentRow.Grade,
        quizScores,
        quizPercentages,
        moduleScores,
        modulePercentages,
        modulePercentiles,
